let a = 1;
let b = 10;

if(a>b){
  document.write("a si es mayor a b esto es un if<br>");
}


// bucles determinados y bucles indeterminados while
while(a<b){
  document.write("a si es mayor a b esto es un while<br>");
  a++;
}

do{
  document.write("a si es mayor a b esto es un while<br>");
  a++;
}while(a<b);

